class Solution {
 public:
  double findMaxAverage(vector<int>& nums, int k) {
    int sum = 0;
    for (int i = 0; i < k; ++i)
      sum += nums[i];
    double ans = 1.0 * sum / k;
    for (int i = 0; i + k < nums.size(); ++i) {
      sum += -nums[i] + nums[i+k];
      ans = max(ans, 1.0 * sum / k);
    }
    return ans;
  }
};

