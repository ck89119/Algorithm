// Definition for a binary tree node.
struct TreeNode {
  int val;
  TreeNode *left;
  TreeNode *right;
  TreeNode(int x) : val(x), left(NULL), right(NULL) {}
};

string int2str(int x) {
  ostringstream os;
  os << x;
  return os.str();
}

class Solution {
 public:
  string tree2str(TreeNode* t) {
    if (t == NULL) return "";

    string ans = int2str(t->val);
    if (t->left == NULL && t->right == NULL) return ans;

    ans += "(";
    ans += tree2str(t->left);
    ans += ")";
    string ans_r = tree2str(t->right);
    if (ans_r != "") {
      ans += "(";
      ans += ans_r;
      ans += ")";
    }
    return ans;
  }
};

