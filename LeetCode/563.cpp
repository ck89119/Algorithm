/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
class Solution {
 public:
  pair<int, int> dfs(TreeNode *node) {
    if (node == NULL) return make_pair(0, 0);

    int s1 = node->val;
    int s2 = 0;
    auto p1 = dfs(node->left);
    auto p2 = dfs(node->right); 
    s1 += p1.first + p2.first;
    s2 += p1.second + p2.second + abs(p1.first - p2.first);
    return make_pair(s1, s2);
  }

  int findTilt(TreeNode* root) {
    auto p = dfs(root);
    return p.second;
  }
};

