class Solution {
 public:
  uint32_t reverseBits(uint32_t n) {
    ll two_pow[32];
    for (int i = 0; i < 32; ++i)
      two_pow[i] = (1 << i);
    
    uint32_t ans = 0;
    for (int i = 0; i < 32; ++i)
      if (n & two_pow[i]) ans += two_pow[31-i];
    return ans;
  }
};

