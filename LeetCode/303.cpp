class NumArray {
 public:
  vector<int> sums;

  NumArray(vector<int> nums) {
    int s = 0;
    for (auto n: nums)
      sums.push_back(s += n);
  }

  int sumRange(int i, int j) {
    if (i == 0) return sums[j];
    else return sums[j] - sums[i-1];
  }
};

