class Solution {
 public:
  int dominantIndex(vector<int>& nums) {
    if (nums.size() < 2) return -1;

    int a, b;
    a = nums[0] > nums[1] ? 0 : 1;
    b = 1 - a;
    for (int i = 2; i < nums.size(); ++i)
      if (nums[i] > nums[a]) {
        b = a;
        a = i;
      } else if (nums[i] > nums[b]) {
        b = i; 
      }
    return nums[a] >= 2 * nums[b] ? a : -1;
  }
};

