class MicrowaveSelling {
public:
	int mostAttractivePrice(int, int);
};

int MicrowaveSelling::mostAttractivePrice(int minPrice, int maxPrice) {
	int i=1,a,b;
	while (i<=maxPrice) i*=10;
	a=i/10;
    b=i/10-1;
    i=maxPrice/a;
	while (i*a+b>maxPrice) i--;
	if (i*a+b<minPrice) return maxPrice;
	else return i*a+b;

}


