class BallAndHats {
public:
	int getHat(string, int);
};

int BallAndHats::getHat(string hats, int numSwaps) {
    int ret;
    if (hats[0]=='o') ret=0;
    else if (hats[1]=='o') ret=1;
    else if (hats[2]=='o') ret=2;
    if (numSwaps==0) return ret;
    else return (numSwaps+ret)%2;
}


