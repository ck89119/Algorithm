class EllysNewNickname {
public:
	int getLength(string nickname) {
		int i,res;
		res=nickname.size();
		for (i=0;i<nickname.size();i++)
            if (check(nickname[i])){
                int j=i+1;
                while (j<nickname.size()&&check(nickname[j])) {res--;j++;}
                i=j-1;
            }
        return res;
	}
};


