class MinimalTriangle {
public:
	double maximalArea(int);
};

double MinimalTriangle::maximalArea(int length) {
	double ret=sqrt(3)/4.0;
	return (ret*length*length);
}


double test0() {
	int p0 = 5;
	MinimalTriangle * obj = new MinimalTriangle();
	clock_t start = clock();
	double my_answer = obj->maximalArea(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	double p1 = 10.825317547305485;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p1 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
