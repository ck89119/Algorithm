class CarrotJumping {
public:
	int theJump(int);
};

int CarrotJumping::theJump(int init) {
    map<int,int> m;
    queue<int> q;
    m[init]=0;
    q.push(init);
    
    while (!q.empty())
    {
          long long x=q.front();
          q.pop();
          if (x==0) return m[x];
          
          long long y=(4*x+3)%1000000007;
          long long z=(8*x+7)%1000000007;
          if (m.find(y)==m.end())
          {
              if (m[x]<100000){
                 m[y]=m[x]+1;
                 q.push(y); }                  
          }
          if (m.find(z)==m.end())
          {
               if (m[x]<100000){
                 m[z]=m[x]+1;
                 q.push(z);  }                 
          }
          
    }
	return -1;
}


