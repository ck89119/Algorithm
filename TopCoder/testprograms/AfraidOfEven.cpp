class AfraidOfEven {
public:
	vector <int> restoreProgression(vector <int>);
	bool make(int n,int d);
    vector <int> num;
};

bool AfraidOfEven::make(int n,int d)
{
     if (n>=num.size()) return true;
     else{
          while (num[n]<(num[n-1]+d)) num[n]*=2;
          if (num[n]>(num[n-1]+d)) return false;
          else return make(n+2,d);                
         }
}

vector <int> AfraidOfEven::restoreProgression(vector <int> seq) {
    num=seq;
	if (make(1,(num[2]-num[0])/2)) return num;
	else 
	{
         
         int d=num[1]-num[0],tmp=num[0];
         while (!make(2,d)) 
         {
               num=seq;
               tmp*=2; 
               num[0]=tmp;            
               d=num[1]-num[0];
         } 
         return num;
    }
}


