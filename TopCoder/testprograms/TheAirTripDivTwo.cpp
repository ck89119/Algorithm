class TheAirTripDivTwo {
public:
	int find(vector <int>, int);
};

int TheAirTripDivTwo::find(vector <int> flights, int fuel) {
	int i=0,ret=0;
	while (i<flights.size())
	{
          if (fuel>=flights[i]) {fuel-=flights[i];ret++;i++;}
          else break;
    }
    return ret;
}


