class SkiResortsEasy {
public:
	int minCost(vector <int> altitude) {
		int sum=0,i;
		for (i=1;i<altitude.size();i++)
            if (altitude[i]>=altitude[i-1]){
                sum+=altitude[i]-altitude[i-1];
                altitude[i]=altitude[i-1];
            }
        return sum;
	}
};


