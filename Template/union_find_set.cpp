class UnionFindSet {
 public:
  int f[N];
  UnionFindSet() {
    for (int i = 0; i < N; ++i)
      f[i] = i;
  }

  int find(int x) {
    if (x == f[x]) return x;
    return f[x] = find(f[x]);
  }

  int merge(int x, int y) {
    f[x = find(x)] = f[y = find(y)];
    return 0;
  }
};

